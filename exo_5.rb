
#{ }  - va réaliser le calcul en fonction des données présentes entre les braquets

puts "On va compter le nombre d'heures de travail à THP"
puts "Travail : #{10 * 5 * 11}"								# fait la multiplication 10 x 5 x 11
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"				# fait la multiplication 10 x 5 x 11 x 60

puts "Et en secondes ?"

puts 10 * 5 * 11 * 60 * 60									# fait la multiplication 10 x 5 x 11 x 60 x 60

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"			# on veut savoir si 3 + 2 est inferieur a -2

puts 3 + 2 < 5 - 7

puts "Ça fait combien 3 + 2 ? #{3 + 2}"						# fait l'addition 3 + 2
puts "Ça fait combien 5 - 7 ? #{5 - 7}"						# fait l'addition 5 - 7 

puts "Ok, c'est faux alors !"								# pour vérifier la condition 3 + 2 < 5 - 7 qui est fausse

puts "C'est drôle ça, faisons-en plus :"

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"			# vérifie si 5 est superieur ou à -2 et nous retourne un booléen vrai
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"		# vérifie si 5 est superieur ou égal à -2 et nous retourne un booléen vrai
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"		# vérifie si 5 est inferieur ou égal à -2 et nous retourne un booléen faux
